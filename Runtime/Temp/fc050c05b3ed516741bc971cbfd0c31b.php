	美国还可以利用区块链技术改善政府机构之间的沟通流程。所有沟通信息都可以被存储和共享，而不是为某一个州和地方国际安全部队提供单一档案。这与数字识别系统相配合，将会减少泄漏，同时增加跨部门机构间的团队合作。
</p>
<p class="MsoNormal">
	&nbsp;
</p>
<p class="MsoNormal">
	区块链技术目前存在的一个挑战也是其最大的潜力，即这是一个全新的、基本不受监管的领域。因此，美国的很多技术尝试，会带来不仅限于国内而领先全球的业务变革。互联网革命早期的重要参与者都在美国，而目前的加密货币和区块链技术并非如此。美国进入这个全新的领域，意味着正赶上世界其他国家的潮流而不再是该领域规则的制定者。
</p>
<p class="MsoNormal">
	&nbsp;
</p>
<p class="MsoNormal">
	结论和挑战
</p>
<p class="MsoNormal">
	&nbsp;
</p>
<p class="MsoNormal">
	<span>当前区块链技术迭代的另一个弱点就是所谓的</span>“最后一英里”问题。加密货币和开放的分布记账技术出现在数字空间中，填补了真实世界和数字世界之间的鸿沟是该领域里人们必须跨越的“最后一英里”，这样才能让技术变得无懈可击和安全。数字领域仍然依赖于真实世界的硬件来保护，并且无论软件的安全性如何，都可以对其进行操作。
</p>
<p class="MsoNormal">
	&nbsp;
</p>
<p class="MsoNormal">
	最终，区块链技术目前的弱点并不会影响美国进入该领域的决心。过去发生过的大规模黑客行为，使国家行为者需要通过新的技术建立强大的安全档案，以防止别有用心之人使国家安全陷入困境。
</p>
